CREATE TABLE Заказчики (
    Наименование_фирмы NVARCHAR(50) PRIMARY KEY,
    Адрес NVARCHAR(100) NOT NULL,
    Расчетный_счет NVARCHAR(20) UNIQUE NOT NULL
);
GO

CREATE TABLE Товары (
    Наименование NVARCHAR(50) PRIMARY KEY,
    Цена REAL CHECK (Цена > 0),
    Количество INT CHECK (Количество >= 0)
);
GO

CREATE TABLE Заказы (
    Номер_заказа INT PRIMARY KEY IDENTITY(1,1),
    Наименование_товара NVARCHAR(50) NOT NULL,
    Цена_продажи REAL CHECK (Цена_продажи > 0),
    Количество INT CHECK (Количество > 0),
    Дата_поставки DATE DEFAULT GETDATE(),
    Заказчик NVARCHAR(50) NOT NULL,
    FOREIGN KEY (Наименование_товара) REFERENCES Товары (Наименование),
    FOREIGN KEY (Заказчик) REFERENCES Заказчики (Наименование_фирмы)
);
GO

